# 6. Body Ratio Pattern Detector
#    "When the candle body is a specific fraction of the range, what follows?"
# ──────────────────────────────────────────────────────────────────────────────

class BodyRatioPatternDetector:
    """
    OTC algorithms can generate candles with suspiciously uniform body-to-range
    ratios.  If candles with body < 20% range are followed by continuation 80%
    of the time, that's a tradeable pattern.
    """

    name = "body_ratio"

    BUCKETS = [
        (0.00, 0.10, "doji"),
        (0.10, 0.25, "small_body"),
        (0.25, 0.50, "medium_body"),
        (0.50, 0.75, "large_body"),
        (0.75, 1.00, "marubozu"),
    ]

    def __init__(self) -> None:
        self._signals: Dict[str, AlgoSignal] = {}

    def fit(self, df: pd.DataFrame) -> None:
        closes = df["close"].values.astype(float)
        opens  = df["open"].values.astype(float)
        highs  = df["high"].values.astype(float)
        lows   = df["low"].values.astype(float)

        bodies = np.abs(closes - opens)
        ranges = (highs - lows) + 1e-9
        ratios = bodies / ranges
        bull   = (closes >= opens).astype(int)

        self._signals = {}
        # Align to (N-1) so we can always look up bull[i+1]
        ratios_prev = ratios[:-1]
        bull_prev   = bull[:-1]

        for (lo, hi, label) in self.BUCKETS:
            for direction in ("bull", "bear"):
                mask = (ratios_prev >= lo) & (ratios_prev < hi)
                if direction == "bull":
                    mask &= bull_prev.astype(bool)
                else:
                    mask &= (~bull_prev.astype(bool))
                indices = np.where(mask)[0]
                if len(indices) < _MIN_N:
                    continue
                next_bull = bull[indices + 1]
                n         = len(next_bull)
                bc        = int(next_bull.sum())
                br        = bc / n
                res       = scipy_stats.binomtest(bc, n, 0.5)
                p         = float(res.pvalue)
                if p >= _P_CUTOFF or abs(br - 0.5) < 0.08:
                    continue
                next_dir = "CALL" if br > 0.5 else "PUT"
                conf     = _calc_conf(br, p, n)
                key      = f"{direction}_{label}"
                self._signals[key] = AlgoSignal(
                    has_pattern=True, direction=next_dir,
                    confidence=conf, detector=self.name,
                    p_value=p, n_samples=n,
                    description=(
                        f"After {direction} {label} candle "
                        f"(body {lo:.0%}-{hi:.0%} of range): "
                        f"next is {next_dir} {br:.0%} of time"
                    ),
                )
        log.debug("BodyRatioPatternDetector: %d patterns", len(self._signals))

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        if not self._signals or len(df) < 2:
            return _NEUTRAL
        c, o = float(df["close"].iloc[-2]), float(df["open"].iloc[-2])
        h, l = float(df["high"].iloc[-2]),  float(df["low"].iloc[-2])
        body  = abs(c - o)
        rng   = (h - l) + 1e-9
        ratio = body / rng
        prev_dir = "bull" if c >= o else "bear"
        for lo, hi, label in self.BUCKETS:
            if lo <= ratio < hi:
                key = f"{prev_dir}_{label}"
                return self._signals.get(key, _NEUTRAL)
        return _NEUTRAL

    def to_dict(self) -> dict:
        return {k: _sig_to_dict(v) for k, v in self._signals.items()}

    def from_dict(self, d: dict) -> None:
        self._signals = {k: _sig_from_dict(v) for k, v in d.items()}


