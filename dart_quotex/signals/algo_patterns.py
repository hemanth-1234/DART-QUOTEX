# 7. Tick Artifact Detector
#    "Does price jump by an unnaturally fixed pip amount repeatedly?"
# ──────────────────────────────────────────────────────────────────────────────

class TickArtifactDetector:
    """
    Synthetic OTC tick generators sometimes produce price movements that are
    exact multiples of a fixed tick size, at regular intervals.

    If the open-to-close move is exactly 3 pips 40% of the time but the
    distribution says it should be 5%, that's a mechanical pattern.
    """

    name = "tick_artifact"

    def __init__(self, pip_size: float = 0.0001) -> None:
        self.pip_size = pip_size
        self._dominant_move_pips: Optional[float] = None
        self._after_exact_move: Optional[AlgoSignal] = None

    def fit(self, df: pd.DataFrame) -> None:
        closes = df["close"].values.astype(float)
        opens  = df["open"].values.astype(float)
        bull   = (closes >= opens).astype(int)

        moves_pips = np.abs(closes - opens) / self.pip_size
        # Round to nearest 0.5 pip
        rounded = np.round(moves_pips * 2) / 2

        from collections import Counter
        counts = Counter(rounded)
        total  = sum(counts.values())
        if total == 0:
            return

        # Find the most overrepresented move size
        expected = 1.0 / len(counts)
        for move, count in counts.most_common(5):
            observed_rate = count / total
            if observed_rate < expected * 3:
                continue
            if count < _MIN_N:
                continue

            mask      = np.abs(rounded - move) < 0.3
            outcomes  = bull[:-1][mask[:-1] if len(mask) > 1 else mask[:len(bull)-1]]
            # Next candle after an exact-move candle
            next_indices = np.where(mask[:-1])[0] + 1
            if len(next_indices) < _MIN_N:
                continue
            next_bull = bull[next_indices]
            n         = len(next_bull)
            bc        = int(next_bull.sum())
            br        = bc / n
            res       = scipy_stats.binomtest(bc, n, 0.5)
            p         = float(res.pvalue)
            if p >= _P_CUTOFF or abs(br - 0.5) < 0.08:
                continue

            direction = "CALL" if br > 0.5 else "PUT"
            conf      = _calc_conf(br, p, n)
            self._dominant_move_pips  = float(move)
            self._after_exact_move    = AlgoSignal(
                has_pattern=True, direction=direction,
                confidence=conf, detector=self.name,
                p_value=p, n_samples=n,
                description=(
                    f"Exact {move:.1f}-pip move occurred: "
                    f"next candle is {direction} {br:.0%} of time "
                    f"(appears {observed_rate:.0%} vs expected {expected:.0%})"
                ),
            )
            break

        log.debug("TickArtifactDetector: dominant_move=%s", self._dominant_move_pips)

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        if self._dominant_move_pips is None or self._after_exact_move is None:
            return _NEUTRAL
        if len(df) < 2:
            return _NEUTRAL
        last_move_pips = (
            abs(float(df["close"].iloc[-2]) - float(df["open"].iloc[-2]))
            / self.pip_size
        )
        if abs(round(last_move_pips * 2) / 2 - self._dominant_move_pips) < 0.4:
            return self._after_exact_move
        return _NEUTRAL

    def to_dict(self) -> dict:
        return {
            "dominant_pips": self._dominant_move_pips,
            "signal":        _sig_to_dict(self._after_exact_move)
                             if self._after_exact_move else None,
        }

    def from_dict(self, d: dict) -> None:
        self._dominant_move_pips = d.get("dominant_pips")
        raw = d.get("signal")
        self._after_exact_move   = _sig_from_dict(raw) if raw else None


