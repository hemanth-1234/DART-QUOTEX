# 8. Run Length Detector
#    "How many consecutive candles in one direction before a reversal?"
# ──────────────────────────────────────────────────────────────────────────────

class RunLengthDetector:
    """
    If an OTC algorithm limits directional runs (e.g. never more than 4
    consecutive bullish candles without a reversal), that limit is detectable.

    Live use: if current run length is approaching the historical maximum
    → signal a reversal.
    """

    name = "run_length"

    def __init__(self) -> None:
        self._max_bull_run:  int   = 0
        self._max_bear_run:  int   = 0
        self._bull_limit_conf: float = 0.0
        self._bear_limit_conf: float = 0.0

    def fit(self, df: pd.DataFrame) -> None:
        closes = df["close"].values.astype(float)
        opens  = df["open"].values.astype(float)
        bull   = (closes >= opens).astype(int)

        def _run_lengths(direction: int) -> List[int]:
            runs, cur = [], 0
            for b in bull:
                if b == direction:
                    cur += 1
                else:
                    if cur > 0:
                        runs.append(cur)
                    cur = 0
            return runs

        bull_runs = _run_lengths(1)
        bear_runs = _run_lengths(0)

        if not bull_runs or not bear_runs:
            return

        self._max_bull_run = int(np.percentile(bull_runs, 95))
        self._max_bear_run = int(np.percentile(bear_runs, 95))

        # Probability of reversal at exactly max run
        def _reversal_conf(runs: List[int], max_r: int) -> float:
            at_limit  = sum(1 for r in runs if r == max_r)
            above_lim = sum(1 for r in runs if r >  max_r)
            total     = at_limit + above_lim
            if total < 5:
                return 0.0
            reversal_rate = at_limit / total
            return float(min(0.90, reversal_rate))

        self._bull_limit_conf = _reversal_conf(bull_runs, self._max_bull_run)
        self._bear_limit_conf = _reversal_conf(bear_runs, self._max_bear_run)

        log.debug(
            "RunLengthDetector: max_bull_run=%d (conf=%.2f) "
            "max_bear_run=%d (conf=%.2f)",
            self._max_bull_run, self._bull_limit_conf,
            self._max_bear_run, self._bear_limit_conf,
        )

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        if not self._max_bull_run or len(df) < 5:
            return _NEUTRAL

        closes  = df["close"].values.astype(float)
        opens   = df["open"].values.astype(float)
        bull    = (closes >= opens).astype(int)
        current_dir = bull[-2]
        run_len = 1
        for i in range(len(bull) - 3, -1, -1):
            if bull[i] == current_dir:
                run_len += 1
            else:
                break

        if current_dir == 1 and run_len >= self._max_bull_run:
            if self._bull_limit_conf >= _MIN_CONF:
                return AlgoSignal(
                    has_pattern=True, direction="PUT",
                    confidence=self._bull_limit_conf, detector=self.name,
                    description=(
                        f"Bull run={run_len} reached historical max ({self._max_bull_run}): "
                        f"reversal expected ({self._bull_limit_conf:.0%})"
                    ),
                )
        if current_dir == 0 and run_len >= self._max_bear_run:
            if self._bear_limit_conf >= _MIN_CONF:
                return AlgoSignal(
                    has_pattern=True, direction="CALL",
                    confidence=self._bear_limit_conf, detector=self.name,
                    description=(
                        f"Bear run={run_len} reached historical max ({self._max_bear_run}): "
                        f"reversal expected ({self._bear_limit_conf:.0%})"
                    ),
                )
        return _NEUTRAL

    def to_dict(self) -> dict:
        return {
            "max_bull":       self._max_bull_run,
            "max_bear":       self._max_bear_run,
            "bull_conf":      self._bull_limit_conf,
            "bear_conf":      self._bear_limit_conf,
        }

    def from_dict(self, d: dict) -> None:
        self._max_bull_run    = d.get("max_bull",  0)
        self._max_bear_run    = d.get("max_bear",  0)
        self._bull_limit_conf = d.get("bull_conf", 0.0)
        self._bear_limit_conf = d.get("bear_conf", 0.0)


# ──────────────────────────────────────────────────────────────────────────────
# Master Scanner — runs all 8 detectors, returns best signal
# ──────────────────────────────────────────────────────────────────────────────

class AlgoPatternScanner:
    """
    Runs all 8 detectors in one scan and at each live candle
    returns the single highest-confidence signal.

    Integration in advisor.py
    --------------------------
        scanner = AlgoPatternScanner()
        scanner.fit(db.get_candles(asset, 60, limit=5000))
        scanner.save("models/algo_patterns.json")

        # Then in every signal loop:
        algo_sig = scanner.signal(df_recent)
        if algo_sig.has_pattern and algo_sig.confidence > 0.65:
            # Use algo_sig.direction alongside AI signal
    """

    def __init__(self, pip_size: float = 0.0001) -> None:
        self._detectors = [
            SequenceDetector(),
            PriceMagnetDetector(pip_size=pip_size),
            OscillationDetector(),
            MeanReversionTimer(),
            VolatilityCycleDetector(),
            BodyRatioPatternDetector(),
            TickArtifactDetector(pip_size=pip_size),
            RunLengthDetector(),
        ]

    def fit(self, df: pd.DataFrame, verbose: bool = True) -> None:
        """Train all detectors on historical data."""
        log.info("AlgoPatternScanner: fitting on %d candles", len(df))
        for det in self._detectors:
            try:
                det.fit(df)
            except Exception as exc:
                log.warning("%s.fit() failed: %s", det.name, exc)
        if verbose:
            log.info("AlgoPatternScanner: training complete")

    def fit_from_db(
        self,
        db,
        asset:       str = "EURUSD_OTC",
        granularity: int = 60,
        limit:       int = 10_000,
    ) -> None:
        """Convenience: load from Database and fit."""
        df = db.get_candles(asset, granularity, limit=limit)
        if df.empty:
            log.warning("No candles in DB for %s", asset)
            return
        self.fit(df)

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        """
        Check all detectors against the current candle context.
        Returns the highest-confidence non-neutral signal, or NEUTRAL.
        """
        candidates = []
        for det in self._detectors:
            try:
                sig = det.signal(df)
                if sig.has_pattern and sig.confidence >= _MIN_CONF:
                    candidates.append(sig)
            except Exception as exc:
                log.debug("%s.signal() error: %s", det.name, exc)

        if not candidates:
            return _NEUTRAL

        # Return highest confidence
        best = max(candidates, key=lambda s: s.confidence)
        log.debug("AlgoPatternScanner best: %s", best)
        return best

    def all_signals(self, df: pd.DataFrame) -> List[AlgoSignal]:
        """Return all active signals above _MIN_CONF threshold (sorted by confidence desc)."""
        results = []
        for det in self._detectors:
            try:
                sig = det.signal(df)
                results.append(sig)
            except Exception as exc:
                log.debug("%s error: %s", det.name, exc)
        return sorted(
            [s for s in results if s.has_pattern and s.confidence >= _MIN_CONF],
            key=lambda s: -s.confidence,
        )

    def save(self, path: str | Path) -> None:
        path = Path(path)
        path.parent.mkdir(parents=True, exist_ok=True)
        data = {}
        for det in self._detectors:
            try:
                data[det.name] = det.to_dict()
            except Exception:
                pass
        path.write_text(json.dumps(data, indent=2), encoding="utf-8")
        log.info("AlgoPatternScanner saved to %s", path)

    def load(self, path: str | Path) -> bool:
        path = Path(path)
        if not path.exists():
            return False
        data = json.loads(path.read_text(encoding="utf-8"))
        for det in self._detectors:
            if det.name in data:
                try:
                    det.from_dict(data[det.name])
                except Exception as exc:
                    log.warning("Could not load %s: %s", det.name, exc)
        log.info("AlgoPatternScanner loaded from %s", path)
        return True

    def report(self, df: pd.DataFrame) -> str:
        """Print-friendly report of all currently active signals."""
        sigs = self.all_signals(df)
        if not sigs:
            return "No algorithmic patterns active for current candle."
        lines = ["=" * 60, "  ACTIVE ALGORITHMIC PATTERNS", "=" * 60]
        for s in sigs:
            mark = "★" if s.confidence > 0.70 else "·"
            lines.append(
                f"  {mark} [{s.detector:<22}]  {s.direction:4}  "
                f"conf={s.confidence:.2f}  | {s.description}"
            )
        lines.append("=" * 60)
        return "\n".join(lines)


