# 3. Oscillation Detector
#    "Does price oscillate between two fixed boundaries repeatedly?"
# ──────────────────────────────────────────────────────────────────────────────

class OscillationDetector:
    """
    Many OTC pricing engines produce price that oscillates between two
    fixed boundaries over a consistent cycle length.

    Detection method:
      · Measure the distance from the current price to the rolling 50-bar high/low
      · If it has touched the boundary and is now moving away → signal

    Live signal: if price just touched the upper boundary → PUT
                 if price just touched the lower boundary → CALL
    """

    name = "oscillation"

    def __init__(self, lookback: int = 50) -> None:
        self.lookback = lookback
        self._touch_threshold: float = 0.02   # within 2% of range = "touching"
        self._upper_reversal_rate: float = 0.5
        self._lower_reversal_rate: float = 0.5
        self._upper_p: float = 1.0
        self._lower_p: float = 1.0
        self._upper_n: int = 0
        self._lower_n: int = 0

    def fit(self, df: pd.DataFrame) -> None:
        closes = df["close"].values.astype(float)
        highs  = df["high"].values.astype(float)
        lows   = df["low"].values.astype(float)
        bull   = (closes[1:] >= closes[:-1]).astype(int)

        n = len(closes)
        upper_reversals, lower_reversals = [], []

        for i in range(self.lookback, n - 1):
            window_high = highs[i - self.lookback : i].max()
            window_low  = lows[i  - self.lookback : i].min()
            band        = window_high - window_low + 1e-9
            pos         = (closes[i] - window_low) / band

            if pos > 1.0 - self._touch_threshold:   # near upper band
                upper_reversals.append(int(bull[i]))
            if pos < self._touch_threshold:          # near lower band
                lower_reversals.append(int(bull[i]))

        def _compute(outcomes: List[int]) -> Tuple[float, float, int]:
            n_ = len(outcomes)
            if n_ < _MIN_N:
                return 0.5, 1.0, n_
            c   = sum(outcomes)
            r   = c / n_
            res = scipy_stats.binomtest(c, n_, 0.5)
            return r, float(res.pvalue), n_

        (self._upper_reversal_rate, self._upper_p,
         self._upper_n) = _compute(upper_reversals)
        (self._lower_reversal_rate, self._lower_p,
         self._lower_n) = _compute(lower_reversals)

        log.debug(
            "OscillationDetector: upper→%s %.0f%% p=%.3f  lower→%s %.0f%% p=%.3f",
            "PUT"  if self._upper_reversal_rate < 0.5 else "CALL",
            self._upper_reversal_rate * 100, self._upper_p,
            "CALL" if self._lower_reversal_rate > 0.5 else "PUT",
            self._lower_reversal_rate * 100, self._lower_p,
        )

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        if len(df) < self.lookback + 2:
            return _NEUTRAL
        highs  = df["high"].values.astype(float)
        lows   = df["low"].values.astype(float)
        closes = df["close"].values.astype(float)
        window_high = highs[-self.lookback - 1 : -1].max()
        window_low  = lows[-self.lookback - 1 : -1].min()
        band        = window_high - window_low + 1e-9
        pos         = (closes[-1] - window_low) / band

        # Near upper band
        if pos > 1.0 - self._touch_threshold:
            if (self._upper_p < _P_CUTOFF and
                    self._upper_reversal_rate < 0.5 - 0.08):
                conf = _calc_conf(1 - self._upper_reversal_rate,
                                  self._upper_p, self._upper_n)
                return AlgoSignal(
                    has_pattern=True, direction="PUT",
                    confidence=conf, detector=self.name,
                    p_value=self._upper_p, n_samples=self._upper_n,
                    description=(
                        f"Price at upper oscillation band ({pos:.0%}): "
                        f"reverts down {1-self._upper_reversal_rate:.0%}"
                    ),
                )

        # Near lower band
        if pos < self._touch_threshold:
            if (self._lower_p < _P_CUTOFF and
                    self._lower_reversal_rate > 0.5 + 0.08):
                conf = _calc_conf(self._lower_reversal_rate,
                                  self._lower_p, self._lower_n)
                return AlgoSignal(
                    has_pattern=True, direction="CALL",
                    confidence=conf, detector=self.name,
                    p_value=self._lower_p, n_samples=self._lower_n,
                    description=(
                        f"Price at lower oscillation band ({pos:.0%}): "
                        f"reverts up {self._lower_reversal_rate:.0%}"
                    ),
                )
        return _NEUTRAL

    def to_dict(self) -> dict:
        return {
            "upper_rate": self._upper_reversal_rate, "upper_p": self._upper_p,
            "upper_n":    self._upper_n,
            "lower_rate": self._lower_reversal_rate, "lower_p": self._lower_p,
            "lower_n":    self._lower_n,
        }

    def from_dict(self, d: dict) -> None:
        self._upper_reversal_rate = d["upper_rate"]
        self._upper_p             = d["upper_p"]
        self._upper_n             = d["upper_n"]
        self._lower_reversal_rate = d["lower_rate"]
        self._lower_p             = d["lower_p"]
        self._lower_n             = d["lower_n"]


