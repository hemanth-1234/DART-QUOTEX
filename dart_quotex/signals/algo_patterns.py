# 5. Volatility Cycle Detector
#    "Volatility expands, then contracts, on a fixed schedule"
# ──────────────────────────────────────────────────────────────────────────────

class VolatilityCycleDetector:
    """
    OTC algorithms often produce volatility on a repeating cycle:
    N candles of high volatility, then M candles of low volatility.

    This detector:
      · Measures ATR at every position in the cycle
      · Finds where ATR is predictably highest (→ larger moves = tradeable)
      · Finds where ATR is predictably lowest (→ skip these candles)
      · Signals the current phase of the cycle
    """

    name = "volatility_cycle"

    def __init__(self, max_period: int = 30) -> None:
        self.max_period = max_period
        self._dominant_period: int = 0
        self._phase_atr: Dict[int, float] = {}     # phase → normalised ATR
        self._high_vol_phases: List[int] = []
        self._low_vol_phases:  List[int] = []

    def fit(self, df: pd.DataFrame) -> None:
        highs  = df["high"].values.astype(float)
        lows   = df["low"].values.astype(float)
        closes = df["close"].values.astype(float)
        ranges = highs - lows

        # Find dominant volatility period via autocorrelation of squared ranges
        sq_r = ranges ** 2
        acorrs = {}
        for p in range(2, self.max_period + 1):
            if len(sq_r) < p * 3:
                continue
            ac = float(pd.Series(sq_r).autocorr(lag=p))
            if not np.isnan(ac):
                acorrs[p] = ac

        if not acorrs:
            return

        self._dominant_period = max(acorrs, key=lambda k: acorrs[k])
        if acorrs[self._dominant_period] < 0.05:
            return   # no meaningful cycle

        p  = self._dominant_period
        n  = len(ranges)
        phase_ranges: Dict[int, List[float]] = defaultdict(list)
        for i in range(n):
            phase_ranges[i % p].append(ranges[i])

        # Normalise ATR per phase
        all_mean = np.mean(ranges)
        self._phase_atr = {}
        for ph, rl in phase_ranges.items():
            self._phase_atr[ph] = float(np.mean(rl) / (all_mean + 1e-9))

        # High vol phases (>= 1.3× average), low vol phases (<= 0.7×)
        self._high_vol_phases = [ph for ph, v in self._phase_atr.items() if v >= 1.3]
        self._low_vol_phases  = [ph for ph, v in self._phase_atr.items() if v <= 0.7]

        log.debug(
            "VolatilityCycleDetector: period=%d high_vol=%s low_vol=%s",
            p, self._high_vol_phases, self._low_vol_phases,
        )

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        if not self._dominant_period or not self._phase_atr:
            return _NEUTRAL

        p            = self._dominant_period
        current_phase = len(df) % p

        atr_ratio = self._phase_atr.get(current_phase, 1.0)

        if current_phase in self._high_vol_phases:
            return AlgoSignal(
                has_pattern=True,
                direction="NEUTRAL",
                confidence=min(0.75, atr_ratio / 2.0),
                detector=self.name,
                description=(
                    f"High-volatility phase (cycle={p}, phase={current_phase}): "
                    f"ATR is {atr_ratio:.1f}× average — larger moves expected"
                ),
                extra={"phase": current_phase, "atr_ratio": atr_ratio,
                       "recommendation": "trade_with_ai"},
            )
        if current_phase in self._low_vol_phases:
            return AlgoSignal(
                has_pattern=True,
                direction="NEUTRAL",
                confidence=0.0,
                detector=self.name,
                description=(
                    f"Low-volatility phase (cycle={p}, phase={current_phase}): "
                    f"ATR is {atr_ratio:.1f}× average — skip this candle"
                ),
                extra={"phase": current_phase, "atr_ratio": atr_ratio,
                       "recommendation": "skip"},
            )
        return _NEUTRAL

    def to_dict(self) -> dict:
        return {
            "period":      self._dominant_period,
            "phase_atr":   {str(k): v for k, v in self._phase_atr.items()},
            "high_vol":    self._high_vol_phases,
            "low_vol":     self._low_vol_phases,
        }

    def from_dict(self, d: dict) -> None:
        self._dominant_period  = d.get("period", 0)
        self._phase_atr        = {int(k): v for k, v in d.get("phase_atr", {}).items()}
        self._high_vol_phases  = d.get("high_vol", [])
        self._low_vol_phases   = d.get("low_vol", [])


