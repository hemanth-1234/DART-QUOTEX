# 2. Price Magnet Detector
#    "Does price cluster around round numbers or specific price levels?"
# ──────────────────────────────────────────────────────────────────────────────

class PriceMagnetDetector:
    """
    OTC pricing engines often exhibit gravitational pull toward round numbers
    (1.10000, 1.10500) or regularly-spaced price levels (every 10 pips).

    This detector:
      · Identifies the most-visited price levels (bins)
      · Measures whether price rebounds from those levels
      · Signals: if current price is near a magnet level and moving toward it
        → expect it to reach and bounce
    """

    name = "price_magnet"

    def __init__(self, pip_size: float = 0.0001, n_bins: int = 100) -> None:
        self.pip_size = pip_size
        self.n_bins   = n_bins
        self._magnet_levels: List[float] = []
        self._bounce_rates: Dict[float, Tuple[float, float, int]] = {}  # level→(bull_rate, p, n)

    def fit(self, df: pd.DataFrame) -> None:
        closes = df["close"].values.astype(float)
        opens  = df["open"].values.astype(float)
        bull   = (closes >= opens).astype(int)

        price_min, price_max = closes.min(), closes.max()
        bin_size = (price_max - price_min) / self.n_bins

        if bin_size < self.pip_size:
            return

        # Count visits per bin
        bins   = np.floor((closes - price_min) / bin_size).astype(int)
        counts = np.bincount(bins, minlength=self.n_bins)
        mean_c = counts.mean()
        std_c  = counts.std() + 1e-9

        # Levels visited significantly more often than average
        magnet_bins = np.where(counts > mean_c + 1.5 * std_c)[0]
        self._magnet_levels = [
            round(price_min + (b + 0.5) * bin_size, 6)
            for b in magnet_bins
        ]

        # For each magnet level: what's the next candle's direction?
        self._bounce_rates = {}
        for level in self._magnet_levels:
            near = np.abs(closes - level) < bin_size * 0.8
            near_indices = np.where(near)[0]
            next_indices = near_indices[near_indices < len(bull) - 1] + 1
            if len(next_indices) < _MIN_N:
                continue
            outcomes   = bull[next_indices]
            bull_count = int(outcomes.sum())
            n          = len(outcomes)
            bull_rate  = bull_count / n
            res        = scipy_stats.binomtest(bull_count, n, 0.5)
            p          = float(res.pvalue)
            if p < _P_CUTOFF and abs(bull_rate - 0.5) > 0.08:
                self._bounce_rates[level] = (bull_rate, p, n)

        log.debug(
            "PriceMagnetDetector: %d magnet levels, %d with bounce signal",
            len(self._magnet_levels), len(self._bounce_rates),
        )

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        if not self._bounce_rates:
            return _NEUTRAL

        current_price = float(df["close"].iloc[-1])
        closes        = df["close"].values.astype(float)
        price_range   = closes.max() - closes.min()
        proximity     = price_range * 0.01  # within 1% of range = "near"

        best: Optional[AlgoSignal] = None
        for level, (bull_rate, p, n) in self._bounce_rates.items():
            dist = abs(current_price - level)
            if dist > proximity:
                continue
            direction = "CALL" if bull_rate > 0.5 else "PUT"
            conf      = _calc_conf(bull_rate, p, n)
            if best is None or conf > best.confidence:
                best = AlgoSignal(
                    has_pattern=True,
                    direction=direction,
                    confidence=conf,
                    detector=self.name,
                    p_value=p,
                    n_samples=n,
                    description=(
                        f"Price magnet at {level:.5f} "
                        f"({dist/self.pip_size:.1f} pips away): "
                        f"{direction} {bull_rate:.0%} on approach"
                    ),
                    extra={"level": level, "distance_pips": dist / self.pip_size},
                )
        return best or _NEUTRAL

    def to_dict(self) -> dict:
        return {
            "magnet_levels": self._magnet_levels,
            "bounce_rates": {
                str(k): {"bull_rate": v[0], "p_value": v[1], "n": v[2]}
                for k, v in self._bounce_rates.items()
            },
        }

    def from_dict(self, d: dict) -> None:
        self._magnet_levels = d.get("magnet_levels", [])
        self._bounce_rates  = {
            float(k): (v["bull_rate"], v["p_value"], v["n"])
            for k, v in d.get("bounce_rates", {}).items()
        }


