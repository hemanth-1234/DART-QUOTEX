# 1. Sequence Detector
#    "After N consecutive bullish candles, what is the next candle?"
# ──────────────────────────────────────────────────────────────────────────────

class SequenceDetector:
    """
    Detects what tends to happen after N candles of the same direction.

    Example:  After exactly 3 consecutive bearish candles, 78% of the time
              the 4th candle is bullish  (p=0.003, n=47)
              → strong CALL signal on the 4th candle
    """

    name = "sequence"

    def __init__(self, max_run: int = 8) -> None:
        self.max_run = max_run
        # Table: {(direction, run_length): [outcomes]}  outcome=1 bullish, 0 bearish
        self._table: Dict[Tuple[str, int], List[int]] = defaultdict(list)
        self._signals: Dict[str, AlgoSignal] = {}

    def fit(self, df: pd.DataFrame) -> None:
        self._table.clear()
        closes  = df["close"].values.astype(float)
        opens   = df["open"].values.astype(float)
        bullish = (closes >= opens).astype(int)

        run     = 0
        run_dir = bullish[0]

        for i in range(1, len(bullish)):
            if bullish[i - 1] == run_dir:
                run += 1
            else:
                run = 1
                run_dir = bullish[i - 1]

            if run <= self.max_run:
                key = ("bull" if run_dir == 1 else "bear", run)
                self._table[key].append(int(bullish[i]))

        self._signals = {}
        for (run_dir, run_len), outcomes in self._table.items():
            n = len(outcomes)
            if n < _MIN_N:
                continue
            bull_count = sum(outcomes)
            bull_rate  = bull_count / n
            res        = scipy_stats.binomtest(bull_count, n, 0.5)
            p          = float(res.pvalue)
            if p >= _P_CUTOFF or abs(bull_rate - 0.5) < 0.08:
                continue
            direction = "CALL" if bull_rate > 0.5 else "PUT"
            conf      = _calc_conf(bull_rate, p, n)
            key       = f"{run_dir}_{run_len}"
            self._signals[key] = AlgoSignal(
                has_pattern=True,
                direction=direction,
                confidence=conf,
                detector=self.name,
                p_value=p,
                n_samples=n,
                description=(
                    f"After {run_len} consecutive {run_dir} candles: "
                    f"next is {direction} {bull_rate:.0%} of the time"
                ),
                extra={"run_dir": run_dir, "run_len": run_len,
                       "bull_rate": bull_rate},
            )
        log.debug("SequenceDetector: %d patterns", len(self._signals))

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        """Check what the current candle run length is and look up signal."""
        if not self._signals:
            return _NEUTRAL
        closes  = df["close"].values.astype(float)
        opens   = df["open"].values.astype(float)
        bullish = (closes >= opens).astype(int)
        if len(bullish) < 2:
            return _NEUTRAL

        # Current run direction and length
        run_dir = "bull" if bullish[-2] == 1 else "bear"
        run_len = 1
        for i in range(len(bullish) - 3, -1, -1):
            if bullish[i] == bullish[-2]:
                run_len += 1
            else:
                break

        key = f"{run_dir}_{run_len}"
        return self._signals.get(key, _NEUTRAL)

    def to_dict(self) -> dict:
        return {k: _sig_to_dict(v) for k, v in self._signals.items()}

    def from_dict(self, d: dict) -> None:
        self._signals = {k: _sig_from_dict(v) for k, v in d.items()}


