# 4. Mean Reversion Timer
#    "How many candles after a large move before price reverts?"
# ──────────────────────────────────────────────────────────────────────────────

class MeanReversionTimer:
    """
    OTC pricing algorithms often implement mean-reversion on a fixed timer:
    after a large directional move (> 1 ATR), price almost always
    reverts within exactly N candles.

    This detector finds the dominant reversion delay N and signals
    a CALL/PUT at exactly the right moment.
    """

    name = "mean_reversion_timer"

    def __init__(self, max_delay: int = 20) -> None:
        self.max_delay = max_delay
        self._reversion_delay: int = 0
        self._after_up_direction:  str   = "NEUTRAL"
        self._after_down_direction: str  = "NEUTRAL"
        self._up_conf:   float = 0.0
        self._down_conf: float = 0.0

    def fit(self, df: pd.DataFrame) -> None:
        closes = df["close"].values.astype(float)
        n      = len(closes)

        rets = np.diff(closes) / (closes[:-1] + 1e-9)
        atr  = np.abs(rets).rolling(14).mean().values \
            if hasattr(np.abs(rets), 'rolling') \
            else _rolling_mean(np.abs(rets), 14)

        delay_stats: Dict[int, List[int]] = defaultdict(list)

        for i in range(14, n - self.max_delay):
            if abs(rets[i]) < atr[i] * 1.5:
                continue
            is_up = rets[i] > 0
            for delay in range(1, self.max_delay + 1):
                j = i + delay
                if j >= len(rets):
                    break
                reverted = (rets[j] < 0) if is_up else (rets[j] > 0)
                delay_stats[delay].append(int(reverted))

        if not delay_stats:
            return

        # Find the delay with the highest reversion rate
        best_delay, best_rate, best_p, best_n = 0, 0.5, 1.0, 0
        for delay, outcomes in delay_stats.items():
            nn  = len(outcomes)
            if nn < _MIN_N:
                continue
            rate = sum(outcomes) / nn
            res  = scipy_stats.binomtest(sum(outcomes), nn, 0.5)
            p    = float(res.pvalue)
            if rate > best_rate and p < _P_CUTOFF:
                best_delay, best_rate, best_p, best_n = delay, rate, p, nn

        self._reversion_delay = best_delay
        if best_delay > 0 and best_rate > 0.5 + 0.08:
            self._after_up_conf   = _calc_conf(best_rate, best_p, best_n)
            self._after_down_conf = self._after_up_conf
        else:
            self._after_up_conf   = 0.0
            self._after_down_conf = 0.0

        log.debug(
            "MeanReversionTimer: best_delay=%d reversion_rate=%.1f%% p=%.4f",
            self._reversion_delay, best_rate * 100, best_p,
        )

    def signal(self, df: pd.DataFrame) -> AlgoSignal:
        if (self._reversion_delay == 0 or
                not hasattr(self, "_after_up_conf") or
                self._after_up_conf < _MIN_CONF):
            return _NEUTRAL

        closes = df["close"].values.astype(float)
        if len(closes) < 16 + self._reversion_delay:
            return _NEUTRAL

        rets = np.diff(closes) / (closes[:-1] + 1e-9)
        atr  = float(np.abs(rets[-14:]).mean())

        # Check if there was a large move exactly `reversion_delay` candles ago
        idx = -(self._reversion_delay + 1)
        if abs(idx) > len(rets):
            return _NEUTRAL

        trigger_ret = rets[idx]
        if abs(trigger_ret) < atr * 1.5:
            return _NEUTRAL

        direction = "PUT" if trigger_ret > 0 else "CALL"
        return AlgoSignal(
            has_pattern=True,
            direction=direction,
            confidence=self._after_up_conf,
            detector=self.name,
            description=(
                f"Large move {self._reversion_delay} candles ago "
                f"({trigger_ret:+.4%}): mean reversion expected → {direction}"
            ),
        )

    def to_dict(self) -> dict:
        return {
            "delay":      self._reversion_delay,
            "up_conf":    getattr(self, "_after_up_conf",   0.0),
            "down_conf":  getattr(self, "_after_down_conf", 0.0),
        }

    def from_dict(self, d: dict) -> None:
        self._reversion_delay = d.get("delay", 0)
        self._after_up_conf   = d.get("up_conf", 0.0)
        self._after_down_conf = d.get("down_conf", 0.0)


