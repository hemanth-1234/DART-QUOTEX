# Helpers
# ──────────────────────────────────────────────────────────────────────────────

def _calc_conf(bias: float, p_value: float, n: int) -> float:
    effect   = abs(bias - 0.5) * 2
    sig      = 1.0 - min(1.0, p_value / _P_CUTOFF)
    sample   = min(1.0, n / (_MIN_N * 5))
    return float(min(0.95, 0.45 * effect + 0.35 * sig + 0.20 * sample))


def _rolling_mean(arr: np.ndarray, w: int) -> np.ndarray:
    out = np.full_like(arr, np.nan)
    for i in range(w - 1, len(arr)):
        out[i] = arr[i - w + 1 : i + 1].mean()
    return out


def _sig_to_dict(s: AlgoSignal) -> dict:
    return {
        "has_pattern": s.has_pattern, "direction": s.direction,
        "confidence":  s.confidence,  "detector":  s.detector,
        "description": s.description, "p_value":   s.p_value,
        "n_samples":   s.n_samples,   "extra":     s.extra,
    }


def _sig_from_dict(d: dict) -> AlgoSignal:
    return AlgoSignal(
        has_pattern=d["has_pattern"], direction=d["direction"],
        confidence=d["confidence"],  detector=d["detector"],
        description=d["description"], p_value=d.get("p_value", 1.0),
        n_samples=d.get("n_samples", 0), extra=d.get("extra", {}),
    )
